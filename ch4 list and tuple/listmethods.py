l1=[1,4,2,7,6,9,12,41]
print (l1)
#sort list in ascending order
l1.sort()
print (l1)
#sort list in descending order
l1.reverse()
print (l1)
#add element at the end of the list
l1.append(56)
print (l1)
#insert element at the index of the list
l1.insert(2,10)
print (l1)
#removing index number element form list
l1.pop(9)
print (l1)
#remove the element from the list
l1.remove(10)
print(l1)